                    case "2":
                        res = "lr-uitheme-accordion";   // 手风琴版本
                        break;
                    case "3":
                        res = "lr-uitheme-windos";      // Windos版本
                        break;
                    case "4":
                        res = "lr-uitheme-top";        // 顶部菜单版本
                        break;
                    default:
                        res = "lr-uitheme-default";     // 经典版本
